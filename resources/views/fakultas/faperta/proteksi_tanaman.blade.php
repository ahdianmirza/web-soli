						<p>
							Sejarah departemen ini diawali pada 1950 sebagai divisi Entomologi-Fitopatologi, Fakultas Pertanian, Universitas Indonesia. Pada 1972, namanya berubah menjadi Departemen Ilmu Hama dan Penyakit Tanaman sebagai bagian dari Fakultas Pertanian, Institut Pertanian Bogor. Departemen ini kembali berganti nama menjadi Departemen Proteksi Tanaman pada 2005. <br>
                            <br> Pada awalnya, Departemen ini hanya menyediakan program strata satu (S1) dan pada 1976, meresmikan program pasca sarjana untuk gelar master dalam Patologi dan Entomologi Tanaman yang diikuti program Doktor pada 1978.
                            <br><br> Program Sarjana menjadi lebih kuat ketika Departemen ini memperoleh hibah melalui Kualitas untuk Pendidikan Sarjana (2000 - 2003); begitu pula dengan program pasca sarjana yang memperoleh hibah melalui Proyek URGE (1997 - 2000). Program studi sarjana memperoleh akreditasi A dari Badan Akreditasi Nasional sejak 1988 hingga 2014. Kinerja Departemen ini diringankan dengan adanya program hibah kompetitif B dari DIKTI (2005 - 2007).
                            <br><br> Proteksi Tanaman telah dikenal sejak lama dalam memainkan peranan penting, dan sebagai agen perubahan, serta mentransfer teknologi dalam pengembangan berbagai strategi pengendalian hama dan penyakit tanaman. Dengan sumber daya manusia yang begitu hebat, berbagai fasilitas serta prestasi di bidang pendidikan, penelitian, dan layanan masyarakat, Departemen ini selalu meningkatkan keteguhannya untuk berkontribusi dalam kebutuhan bangsa.
